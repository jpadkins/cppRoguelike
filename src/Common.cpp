void DrawAndTransform::matchLastMouseMovement()
{
    auto position = getPosition();

    setPosition(
        position.x + (State::get().mousePosition.x -
            State::get().lastMousePosition.x),
        position.y + (State::get().mousePosition.y -
            State::get().lastMousePosition.y)
    );
}
