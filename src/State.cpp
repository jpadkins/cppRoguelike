///////////////////////////////////////////////////////////////////////////////
/// @file   State.hpp
/// @author Jacob Adkins (jpadkins)
/// @brief  Global singleton class containing references to different
///         management classes for the roguelike game
///////////////////////////////////////////////////////////////////////////////

#include "State.hpp"
#include "FrameManager.hpp"

///////////////////////////////////////////////////////////////////////////////
State& State::get()
{
    static State state;
    return state;
}

///////////////////////////////////////////////////////////////////////////////
void State::update()
{
    m_frameManager->update();
}

///////////////////////////////////////////////////////////////////////////////
State::State() : m_frameManager(new FrameManager()) {}

///////////////////////////////////////////////////////////////////////////
void State::draw(sf::RenderTarget& target, sf::RenderStates) const
{
    target.draw(*m_frameManager);
}
