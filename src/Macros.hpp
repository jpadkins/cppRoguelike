#include <iostream>

///////////////////////////////////////////////////////////////////////////////
/// Logging
///////////////////////////////////////////////////////////////////////////////
#define log_info(msg) std::clog<<"[EXIT]["<<__FILE__<<"]["<<__func__<<"]["\
    <<__LINE__<<"]: "<<msg<<std::endl;
#define log_warn(msg) std::cerr<<"[EXIT]["<<__FILE__<<"]["<<__func__<<"]["\
    <<__LINE__<<"]: "<<msg<<std::endl;
#define log_exit(msg) std::cerr<<"[EXIT]["<<__FILE__<<"]["<<__func__<<"]["\
    <<__LINE__<<"]: "<<msg<<std::endl;exit(-1);

///////////////////////////////////////////////////////////////////////////////
/// Misc
///////////////////////////////////////////////////////////////////////////////
#define UNUSED(x) (void)x

#endif
